package edu.upvictoria.graphbuilder.Figuras;

public class CircleCenter {
    private double mX;
    private double mY;

    public CircleCenter(double mX, double mY) {
        this.mX = mX;
        this.mY = mY;
    }

    public double getX() {
        return mX;
    }

    public double getY() {
        return mY;
    }

    public void setmX(double mX) {
        this.mX = mX;
    }

    public void setmY(double mY) {
        this.mY = mY;
    }
}
